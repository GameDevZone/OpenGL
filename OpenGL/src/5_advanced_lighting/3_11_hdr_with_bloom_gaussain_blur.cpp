#include <iostream>


#define WINDOW_WIDTH 1280
#define WINDOW_HEIGHT 720

const unsigned int SHADOW_WIDTH = 1024 * 2, SHADOW_HEIGHT = 1024 * 2;

void framebuffer_size_callback(GLFWwindow *Window, int width, int height);
void processInput(GLFWwindow *window);
void mouse_callback(GLFWwindow* window, double xpos, double ypos);
void scroll_callback(GLFWwindow* window, double xoffset, double yoffset);
unsigned int loadTexture(const char *path, bool gamma);
unsigned int loadSkyBox(std::vector<std::string> faces);
unsigned int createFrameBuffer(unsigned int textureColorBuffer, unsigned int renderBuffer, GLenum attch);
unsigned int createTextureBuffer(GLint internalFormat, GLenum format, GLenum type);
unsigned int createRenderBuffer(GLenum internalformat);
unsigned int createFloatFrameBuffer();
void renderCube();
void renderQuad();

// camera 
Camera camera(glm::vec3(3.5f, 0.50f, 4.30f), glm::vec3(0.0, 1.0, 0.0), -120.0);
bool firstMouse = true;
float lastX = WINDOW_WIDTH / 2.0;
float lastY = WINDOW_WIDTH / 2.0;

// delta time
float deltaTime = .0f;
float lastTime = .0f;

// bloom: high dynamic range
bool bloom = true;
bool bloomKeyPressed = false;
float exposure = 1.0f;

// bloom frame buffer
unsigned int bloomFBO;

bool renderColorBufferSwitch = true;
bool renderColorBufferPressed = false;

int main(void)
{
	GLFWwindow* window;

	if (!glfwInit())
		return -1;

	glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
	glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 3);
	glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);

#ifdef __APPLE__
	glfwWindowHint(GLFW_OPENGL_FORWARD_COMPAT, GL_TRUE);
#endif // __APPLE__


	/* Create a windowed mode window and its OpenGL context */
	window = glfwCreateWindow(WINDOW_WIDTH, WINDOW_HEIGHT, "Learn OpenGL", NULL, NULL);
	if (!window)
	{
		std::cout << "Failed To Create GLFW Window" << std::endl;
		glfwTerminate();
		return -1;
	}

	glfwMakeContextCurrent(window);

	if (!gladLoadGLLoader((GLADloadproc)glfwGetProcAddress))
	{
		std::cout << "Failed to initialize GLAD" << std::endl;
		return -1;
	}

	glViewport(0, 0, WINDOW_WIDTH, WINDOW_HEIGHT);

	glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_DISABLED);
	glfwSetFramebufferSizeCallback(window, framebuffer_size_callback);
	glfwSetCursorPosCallback(window, mouse_callback);
	glfwSetScrollCallback(window, scroll_callback);
	glEnable(GL_DEPTH_TEST);

	// lighting info
	// -------------
	// positions
	std::vector<glm::vec3> lightPositions;
	lightPositions.push_back(glm::vec3(0.0f, 0.5f, 1.5f));
	lightPositions.push_back(glm::vec3(-4.0f, 0.5f, -3.0f));
	lightPositions.push_back(glm::vec3(3.0f, 0.5f, 1.0f));
	lightPositions.push_back(glm::vec3(-.8f, 2.4f, -1.0f));
	// colors
	std::vector<glm::vec3> lightColors;
	lightColors.push_back(glm::vec3(2.0f, 2.0f, 2.0f));
	lightColors.push_back(glm::vec3(2.5f, 0.0f, 0.0f));
	lightColors.push_back(glm::vec3(0.0f, 0.0f, 2.5f));
	lightColors.push_back(glm::vec3(0.0f, 2.5f, 0.0f));

	
	Shader bloomShader(
		"shader/5_section_shaders/3_11_bloom_final.vs",
		"shader/5_section_shaders/3_11_bloom_final.fs");
	
	// render cube
	Shader cubeObject(
		"shader/5_section_shaders/3_11_lighting_2_out.vs",
		"shader/5_section_shaders/3_11_lighting_2_out_cubebox.fs");

	// render light object
	Shader lightObject(
		"shader/5_section_shaders/3_11_lighting_2_out.vs",
		"shader/5_section_shaders/3_11_lighting_2_out_lightobject.fs");

	// render light object
	Shader gaussinBlur(
		"shader/5_section_shaders/3_11_gaussian_blur.vs",
		"shader/5_section_shaders/3_11_gaussian_blur.fs");

	unsigned int brickTexture = loadTexture("Resources/textures/wood.png", true);
	unsigned int containerTexture = loadTexture("Resources/textures/container2.png", true);
	
	cubeObject.Use();
	cubeObject.SetInt("diffuseTexture", 0);
	gaussinBlur.Use();
	gaussinBlur.SetInt("image", 0);
	bloomShader.Use();
	bloomShader.SetInt("scene", 0);
	bloomShader.SetInt("blur", 1);
	
	// create frame buffer
	unsigned int bloomFBO;
	glGenFramebuffers(1, &bloomFBO);
	glBindFramebuffer(GL_FRAMEBUFFER, bloomFBO);

	// create two color buffer
	unsigned int colorBuffers[2];
	glGenTextures(2, colorBuffers);
	for (unsigned int i = 0; i < 2; i ++)
	{
		glBindTexture(GL_TEXTURE_2D, colorBuffers[i]);
		glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA16F, WINDOW_WIDTH, WINDOW_HEIGHT, 0, GL_RGB, GL_FLOAT, NULL);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);

		glFramebufferTexture2D(GL_FRAMEBUFFER, GL_COLOR_ATTACHMENT0 + i, GL_TEXTURE_2D, colorBuffers[i], 0);
	}

	// render buffer
	unsigned int renderBuffer;
	glGenRenderbuffers(1, &renderBuffer);
	glBindRenderbuffer(GL_RENDERBUFFER, renderBuffer);
	glRenderbufferStorage(GL_RENDERBUFFER, GL_DEPTH_COMPONENT, WINDOW_WIDTH, WINDOW_HEIGHT);

	glFramebufferRenderbuffer(GL_FRAMEBUFFER, GL_DEPTH_ATTACHMENT, GL_RENDERBUFFER, renderBuffer);
	unsigned int attachments[2] = { GL_COLOR_ATTACHMENT0, GL_COLOR_ATTACHMENT1 };
	glDrawBuffers(2, attachments);
	if (glCheckFramebufferStatus(GL_FRAMEBUFFER) != GL_FRAMEBUFFER_COMPLETE)
		std::cout << "ERROR:: FRAMEBUFFER NOT SUCCESS!" << std::endl;

	glBindFramebuffer(GL_FRAMEBUFFER, 0);

	// two frame buffer for gaussain blur
	unsigned int pingpontFBO[2];
	unsigned int imagebuffer[2];
	glGenFramebuffers(2, pingpontFBO);
	glGenTextures(2, imagebuffer);
	for (unsigned int i = 0; i < 2; i ++)
	{
		glBindFramebuffer(GL_FRAMEBUFFER, pingpontFBO[i]);
		glBindTexture(GL_TEXTURE_2D, imagebuffer[i]);
		glTexImage2D(GL_TEXTURE_2D, 0, GL_RGB16F, WINDOW_WIDTH, WINDOW_HEIGHT, 0, GL_RGB, GL_FLOAT, NULL);

		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);

		glFramebufferTexture2D(GL_FRAMEBUFFER, GL_COLOR_ATTACHMENT0, GL_TEXTURE_2D, imagebuffer[i], 0);
		if (glCheckFramebufferStatus(GL_FRAMEBUFFER) != GL_FRAMEBUFFER_COMPLETE)
			std::cout << "Framebuffer not complete!" << std::endl;
	}

	glBindFramebuffer(GL_FRAMEBUFFER, 0);

	while (!glfwWindowShouldClose(window))
	{
		float currentFrame = (float)glfwGetTime();
		deltaTime = currentFrame - lastTime;
		lastTime = currentFrame;
		/* key input */
		processInput(window);


		glClearColor(0.2f, 0.2f, 0.2f, 1.f);
		glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

		// render to bloomFrameBuffer
		glBindFramebuffer(GL_FRAMEBUFFER, bloomFBO);
		glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
				
		//reset window
		cubeObject.Use();
		glm::mat4 projection = glm::perspective(glm::radians(camera.Zoom), (float)WINDOW_WIDTH / (float)WINDOW_HEIGHT, 0.1f, 100.0f);
		glm::mat4 view = camera.GetViewMatrix();
		cubeObject.SetMat4("projection", projection);
		cubeObject.SetMat4("view", view);

		for (unsigned int i = 0; i < lightPositions.size(); i++)
		{
			cubeObject.SetVec3("lights[" + std::to_string(i) + "].Position", lightPositions[i]);
			cubeObject.SetVec3("lights[" + std::to_string(i) + "].Color", lightColors[i]);
		}

		// floor
		glActiveTexture(GL_TEXTURE0);
		glBindTexture(GL_TEXTURE_2D, brickTexture);

		glm::mat4 model = glm::mat4(1.0f);
		model = glm::translate(model, glm::vec3(0.0f, -1.0f, 0.0));
		model = glm::scale(model, glm::vec3(12.5f, 0.5f, 12.5f));
		cubeObject.SetMat4("model", model);
		cubeObject.SetMat4("model", model);
		renderCube();
		// then create multiple cubes as the scenery
		glBindTexture(GL_TEXTURE_2D, containerTexture);
		model = glm::mat4(1.0f);
		model = glm::translate(model, glm::vec3(0.0f, 1.5f, 0.0));
		model = glm::scale(model, glm::vec3(0.5f));
		cubeObject.SetMat4("model", model);
		renderCube();

		model = glm::mat4(1.0f);
		model = glm::translate(model, glm::vec3(2.0f, 0.0f, 1.0));
		model = glm::scale(model, glm::vec3(0.5f));
		cubeObject.SetMat4("model", model);
		renderCube();

		model = glm::mat4(1.0f);
		model = glm::translate(model, glm::vec3(-1.0f, -1.0f, 2.0));
		model = glm::rotate(model, glm::radians(60.0f), glm::normalize(glm::vec3(1.0, 0.0, 1.0)));
		cubeObject.SetMat4("model", model);
		renderCube();

		model = glm::mat4(1.0f);
		model = glm::translate(model, glm::vec3(0.0f, 2.7f, 4.0));
		model = glm::rotate(model, glm::radians(23.0f), glm::normalize(glm::vec3(1.0, 0.0, 1.0)));
		model = glm::scale(model, glm::vec3(1.25));
		cubeObject.SetMat4("model", model);
		renderCube();

		model = glm::mat4(1.0f);
		model = glm::translate(model, glm::vec3(-2.0f, 1.0f, -3.0));
		model = glm::rotate(model, glm::radians(124.0f), glm::normalize(glm::vec3(1.0, 0.0, 1.0)));
		cubeObject.SetMat4("model", model);
		renderCube();

		model = glm::mat4(1.0f);
		model = glm::translate(model, glm::vec3(-3.0f, 0.0f, 0.0));
		model = glm::scale(model, glm::vec3(0.5f));
		cubeObject.SetMat4("model", model);
		renderCube();

		// render light object
		lightObject.Use();
		lightObject.SetMat4("projection", projection);
		lightObject.SetMat4("view", view);

		for (unsigned int i = 0; i < lightPositions.size(); i++)
		{
			model = glm::mat4(1.0f);
			model = glm::translate(model, glm::vec3(lightPositions[i]));
			model = glm::scale(model, glm::vec3(0.25f));
			lightObject.SetMat4("model", model);
			lightObject.SetVec3("lightColor", lightColors[i]);
			renderCube();
		}
		glBindFramebuffer(GL_FRAMEBUFFER, 0);

		// blur
		bool horizontal = true, first_iteration = true;
		unsigned int amout = 10;
		gaussinBlur.Use();
		for (unsigned int i = 0 ; i < amout; i ++)
		{
			glBindFramebuffer(GL_FRAMEBUFFER, pingpontFBO[horizontal]);
			gaussinBlur.SetInt("horizontal", horizontal);
			glBindTexture(GL_TEXTURE_2D, first_iteration ? colorBuffers[1] : imagebuffer[!horizontal]);
			renderQuad();
			horizontal = !horizontal;
			if (first_iteration)
				first_iteration = false;
		}

		// render to normal frame buffer
		glBindFramebuffer(GL_FRAMEBUFFER, 0);
		glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
		bloomShader.Use();
		glActiveTexture(GL_TEXTURE0);
		glBindTexture(GL_TEXTURE_2D, colorBuffers[0]);
		glActiveTexture(GL_TEXTURE1);
		glBindTexture(GL_TEXTURE_2D, imagebuffer[!horizontal]);
		bloomShader.SetInt("bloom", bloom);
		bloomShader.SetFloat("exposure", exposure);
		renderQuad();
		std::cout << "Bloom: " << (bloom ? "on" : "off") << "| exposure: " << exposure << std::endl;

		glfwSwapBuffers(window);
		glfwPollEvents();
	}

	/* clear all the stuff */
	glfwTerminate();
	return 0;
}
